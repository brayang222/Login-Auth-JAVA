    private void cargarTabla() {
           DefaultTableModel modeloTabla = new DefaultTableModel(){
               @Override
               public boolean isCellEditable (int row, int column){
                   return false;
               }
           };
           //Establecemos los nombres
           String titulos[] = {"Id", "Usuario", "Contra", "Rol"};
           modeloTabla.setColumnIdentifiers(titulos);
           //Traer de la bd la lista de usuarios
           List<Usuario> listaUsuarios = control.traerUsuarios();
           if (listaUsuarios != null) {
               for(Usuario usu : listaUsuarios) {
                   Object[] objeto = {usu.getId(), usu.getNombreUsuario(), 
                       usu.getContra(), usu.getUnRol().getNombreRol()}; 
                   
                   //ingresa los datos de cada uno de los usuarios
                   modeloTabla.addRow(objeto);
                }    
            }
            tablaUsuarios.setModel(modeloTabla);
    }
    
