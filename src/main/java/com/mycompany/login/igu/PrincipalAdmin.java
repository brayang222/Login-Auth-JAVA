    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(tablaUsuarios.getRowCount() > 0) {
            if (tablaUsuarios.getSelectedRow() != -1) {
                //obtener id del elemento
                int idUsuario = Integer.parseInt(String.valueOf(tablaUsuarios
                        .getValueAt(tablaUsuarios.getSelectedRow(), 0)));
                
                // llamo al metodo borrar
                control.borrarUsuario(idUsuario);
                mostrarMensaje("Se borro el usuario correctamente",
                        "Info", "Eliminacion correcta");
                cargarTabla();
            } else {
                mostrarMensaje("No selecciono ningun registro",
                        "Error", "Error al borrar");
            }
        } else {
            mostrarMensaje("La tabla esta vacia",
                    "Error", "Error al borrar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        if(tablaUsuarios.getRowCount() > 0) {
            if (tablaUsuarios.getSelectedRow() != -1) {
                 int idUsuario = Integer.parseInt(String.valueOf(tablaUsuarios
                        .getValueAt(tablaUsuarios.getSelectedRow(), 0)));
                 
                 // llamo a la ventana de edicion
                 EdicionUsuarios pantallaEdicion = new EdicionUsuarios(control, idUsuario);
                 pantallaEdicion.setVisible(true);;
                 pantallaEdicion.setLocationRelativeTo(null);
                 
            } else {
                mostrarMensaje("No selecciono ningun registro",
                        "Error", "Error al editar");
            }
        } else {
            mostrarMensaje("La tabla esta vacia",
                    "Error", "Error al editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

